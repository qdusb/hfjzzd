<P>（一）组织参加最高法院、省高级法院和上级机关安排的培训</P>
<P>根据最高人民法院和省高级人民法院，以及省市有关部门下发的培训计划方案和通知，我市法院将派送人员参加相应的培训。其中包括：</P>
