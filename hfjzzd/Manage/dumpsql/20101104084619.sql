<P>2、省市委党校调学和公务员培训。</P>
<P>3、全省中、基层法院领导班子成员培训班。省高院计划于5-9月份分批组织，分为分管刑事审判副院长、分管民事审判副院长、分管其他审判工作副院长、政治部主任、纪检组长5期培训。</P>
<P>4、省高院组织的各专业审判业务培训班，包括刑事审判业务培训班、民事审判业务培训班、行政审判业务培训班、立案信访业务培训班四个班次。针对新法律、司法解释的理解适用，审判工作中的重点、难点问题，以专题讲座为主要形式展开培训。</P>
<P>5、预备法官培训班。3月至10月份，通过司法考试并取得资格，符合任命法官条件的人员，将依照《法官培训条例》规定，参加省高院统一组织的预备法官培训班培训。</P>
<P>（二）拟晋升高级法官人员培训</P>
<P>组织16名将符合晋升四级高级法官条件的法官，参加全国网络晋高培训。</P>
<P>（三）业务讲座及专业论坛（四季度前）</P>
<P>组织去年以来参加国家法官学院培训和其他较高层次培训的同志备课，举办全市法院专业审判业务讲座。</P>
<P>结合“案件质量管理年”活动，针对审判、执行工作中的热点难点问题，及改判、发回重审案件中经常发生的问题，举办刑事审判、民商事审判专业法官论坛。专业论坛在全市法院范围内按业务分类组织开展，由相应的业务庭室负责人组织和主持，并确定主讲人员和论坛主题。</P>
<P>（四）组织法官考察学习</P>
<P>法官分院会同法官协会，以审判一线法官为主，分批、分层次组织部分法官到外地考察学习，开展业务交流，拓宽法官视野，全面提升司法水平和司法能力。</P>
<P>（五）司法警察培训（六、七月份）。</P>
<P>按照最高人民法院关于《人民法院司法警察训练大纲》、《人民法院司法警察暂行条例》规定，分二部分进行培训：第一部分主要进行业务技能培训，重点进行队列、拳术、警棍械使用、六四手枪射击训练及规范化庭审动作训练。第二部分学习与司法警察工作有关的法律知识，提高执法水平。</P>
<P>（六）年轻干警理念教育培训（三季度）</P>
<P>对近几年新进法院工作的年轻干警，进行社会主义法治理念、法官行为规范、法官礼仪、法律思维的基本原则、职业技能等培训。引导年轻干警端正工作作风，正确认识人民法院工作的职责、任务，做坚定的中国特色社会主义事业的建设者、捍卫者。</P>
